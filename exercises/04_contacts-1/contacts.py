"""
Stampa i dettagli di un contatto definito nel codice.
"""


name = "Paolo"
surname = "Rossi"
phone = "333-333-333"
email = "paolo.rossi@example.com"

print(f"Contatto: {name} {surname}")
print(f"Telefono: {phone}")
print(f"Email: {email}")

print(f"""Contatto: {name} {surname}
Telefono: {phone}
Email: {email}""")

print(f"Contatto: {name} {surname}\nTelefono: {phone}\nEmail: {email}")
