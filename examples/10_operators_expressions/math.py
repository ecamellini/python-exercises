"""
Operatori matematici.
"""

print(5 + 3)  # somma - risultato: 8
print('Hello' + ' ' + 'World!')  # concatenazione - risultato: 'Hello World!'
print(5 - 3)  # sottrazione - risultato: 2
print(-3.5)  # numero negativo - risultato: -3.5
print(5 * 3)  # moltiplicazione - risultato: 15
print('la' * 3)  # ripetizione - risultato: 'lalalala'
print(5 ** 3)  # potenza - risultato: 125
print(13 / 3)  # divisione - risultato: 4.333333333333333
print(13 // 3)  # divisione intera - risultato: 4 - arrotonda per difetto
print(13.0 // 3)  # divisione intera - risultato: 4.0 - uguale, ma Ã¨ un float
print(13 % 3)  # resto/modulo - risultato: 1
print(25.5 % 2.25)  # resto/modulo - risultato: 0.75
